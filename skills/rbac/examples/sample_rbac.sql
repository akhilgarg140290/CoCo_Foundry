-- ============================================================================
-- SECTION 2: DYNAMIC DATA MASKING POLICIES
-- ============================================================================
-- Masking policies reference the database role for unmask exemption.

USE ROLE SYSADMIN;

CREATE OR REPLACE MASKING POLICY PROD_SILVER_DB.COMMON.SSN_MASK AS
    (val STRING) RETURNS STRING ->
    CASE
        WHEN IS_DATABASE_ROLE_IN_SESSION('PROD_SILVER_DB.AR_UNMASK') THEN val
        ELSE '***-**-' || RIGHT(val, 4)
    END
    COMMENT = 'Partial mask for SSN — shows last 4 digits only';

CREATE OR REPLACE MASKING POLICY PROD_SILVER_DB.COMMON.EMAIL_MASK AS
    (val STRING) RETURNS STRING ->
    CASE
        WHEN IS_DATABASE_ROLE_IN_SESSION('PROD_SILVER_DB.AR_UNMASK') THEN val
        ELSE '****@' || SPLIT_PART(val, '@', 2)
    END
    COMMENT = 'Masks email username, preserves domain';

CREATE OR REPLACE MASKING POLICY PROD_SILVER_DB.COMMON.PHONE_MASK AS
    (val STRING) RETURNS STRING ->
    CASE
        WHEN IS_DATABASE_ROLE_IN_SESSION('PROD_SILVER_DB.AR_UNMASK') THEN val
        ELSE '***-***-' || RIGHT(val, 4)
    END
    COMMENT = 'Partial mask for phone — shows last 4 digits';

CREATE OR REPLACE MASKING POLICY PROD_SILVER_DB.COMMON.NAME_MASK AS
    (val STRING) RETURNS STRING ->
    CASE
        WHEN IS_DATABASE_ROLE_IN_SESSION('PROD_SILVER_DB.AR_UNMASK') THEN val
        ELSE '** MASKED **'
    END
    COMMENT = 'Full mask for patient names';

CREATE OR REPLACE MASKING POLICY PROD_SILVER_DB.COMMON.DOB_MASK AS
    (val DATE) RETURNS DATE ->
    CASE
        WHEN IS_DATABASE_ROLE_IN_SESSION('PROD_SILVER_DB.AR_UNMASK') THEN val
        ELSE DATE_FROM_PARTS(YEAR(val), 1, 1)
    END
    COMMENT = 'Masks DOB to year only';

