-- ============================================================================
-- SECTION 1: ACCESS ROLES (DATABASE ROLES)
-- ============================================================================
-- Database roles are created by the database owner (SYSADMIN).
-- They automatically have USAGE on their owning database — no explicit grant needed.

USE ROLE SYSADMIN;

